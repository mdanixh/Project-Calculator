function sum(num1: number, num2: number) {
    return num1 + num2;
}
function sub(num1: number, num2: number) {
    return num1 - num2;
}
function mul(num1: number, num2: number) {
    return num1 * num2;
}
function div(num1: number, num2: number) {
    return num1 / num2;
}

export {sum, sub, mul, div}